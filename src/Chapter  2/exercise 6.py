import math
# Get values from user
x1 = input ("X1:")
x2 = input ("X2:")
y1 = input ("Y1:")
y2 = input ("Y2:")
# Convert to a float
a = float (x1)
b = float (x2)
c = float (y1)
d = float (y2)
# Assign to calculation
e = b-a
f = d-c
# Square the values
g = e**2
h = f**2
#Add the values
i = g+h
#Square root of answer
print (math.sqrt(i) , "cm")
